export class Calculator {
    public sum = (firstNumber: number, secondNumber: number): number => firstNumber + secondNumber;

    public subtract = (firstNumber: number, secondNumber: number): number => firstNumber - secondNumber;

    public multiply = (firstNumber: number, secondNumber: number): number => firstNumber * secondNumber;

    public divide = (firstNumber: number, secondNumber: number): number => firstNumber / secondNumber;
}